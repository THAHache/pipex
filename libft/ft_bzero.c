#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*aux;

	i = 0;
	aux = (char *)s;
	if (n > 0)
	{
		while (i < n)
		{
			aux[i] = 0;
			i++;
		}
	}
}
